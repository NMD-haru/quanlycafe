    private void LoadData2Table() throws SQLException, ClassNotFoundException {
        List<user> b = dao.daouser.getAllND();
        //--- Thêm các tiêu đề cho cột của bảng hiển thị
        tblModel = new DefaultTableModel();
        tblModel.addColumn("id");
        tblModel.addColumn("tên đăng nhập");
        //--- add lần lượt các row
        for (user user1 : b) {
            Vector<String> row = new Vector<>();
            row.addElement(String.valueOf(user1.getId()));
            row.addElement(user1.getTaikhoan());
            tblModel.addRow(row);
        }
        jXTable1.setModel(tblModel);
    }

