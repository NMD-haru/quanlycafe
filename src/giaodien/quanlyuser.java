    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jXTable1.getModel();
        user nv1 = new user();
        int j = jXTable1.getSelectedRow();
        int i = jXTable1.convertRowIndexToModel(j);

        if (j != -1) {
            try {
                nv1.setId(Integer.parseInt((String) dtm.getValueAt(i, 0)));
                daouser.DeleteNV(nv1);
                LoadData2Table();
            } catch (SQLException ex) {
                Logger.getLogger(quanlyuser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(quanlyuser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_delActionPerformed

    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        if (txtusername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập tên đăng nhập!");
            return;
        }
        if (txtusername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập tên đăng nhập!");
            return;
        }
        for(int i=0;i<jXTable1.getRowCount();i++){
        if (txtusername.getText().equals(jXTable1.getValueAt(i,1))) {
            
            JOptionPane.showMessageDialog(null, "Tài khoản đã tồn tại!");
            return;
        }
        }
        if (txtrepass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chưa nhập xác nhận mật khẩu!");
            return;
        }
        if (!txtpass.getText().equals(txtrepass.getText())) {
            JOptionPane.showMessageDialog(null, "Xác nhận mật khẩu chưa chính xác!");
        } else {
            try {
                user newuser = new user();
                newuser.setTaikhoan(txtusername.getText());
                newuser.setMatkhau(txtpass.getText());
                dao.daouser.InsertNV(newuser);
                txtusername.setText("");
                txtpass.setText("");
                txtrepass.setText("");
                LoadData2Table();
            } catch (SQLException ex) {
                Logger.getLogger(quanlyuser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(quanlyuser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_addMouseClicked

