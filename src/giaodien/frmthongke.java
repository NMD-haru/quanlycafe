    private void btTimKiemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btTimKiemMouseClicked
        try {
            // call methods that might throw SQLException
            String timkiem = txtTimKiem.getText();
            List<thongke> c = daoThongKe.TKNV(timkiem);
            tblModel.setRowCount(0);
            for (thongke tk2 : c) {
                Vector<String> row = new Vector<>();
                row.addElement(String.valueOf(tk2.getManv()));
                row.addElement(tk2.getTennv());
                row.addElement(String.valueOf(tk2.getNgaysinh()));
                row.addElement(tk2.getSdt());
                row.addElement(String.valueOf(tk2.getNgaycong()));
                row.addElement(String.valueOf(tk2.getHesoluong()));
                row.addElement(String.valueOf(tk2.getLuong()));
                tblModel.addRow(row);
            }
            dtTTNV.setModel(tblModel);
        } catch (SQLException e) {
            // do something appropriate with the exception, at least:
            e.printStackTrace();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmthongke.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btTimKiemMouseClicked

