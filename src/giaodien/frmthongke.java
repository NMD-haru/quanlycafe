    private void btNgayCongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btNgayCongMouseClicked
        // TODO add your handling code here:

        DefaultTableModel dtm = (DefaultTableModel) dtTTNV.getModel();
        int j = dtTTNV.getSelectedRow();
        if (j != -1) {
            try {
                int i = dtTTNV.convertRowIndexToModel(j);
                thongke tk = new thongke();
                tk.setManv((String) dtm.getValueAt(i, 0));
                tk.setNgaycong(Integer.parseInt(jTextField1.getText()));
                daoThongKe.UpdateBangLuong(tk);
                jTextField1.setText("");
                LoadData2Table();
            } catch (SQLException ex) {
                Logger.getLogger(frmthongke.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmthongke.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btNgayCongMouseClicked

