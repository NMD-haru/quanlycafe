    public void LoadData2Table() throws SQLException, ClassNotFoundException {
        List<thongke> C = dao.daoThongKe.TTTK();
        //--- Thêm các tiêu đề cho cột của bảng hiển thị
        tblModel = new DefaultTableModel();
        tblModel.addColumn("mã nhân viên");
        tblModel.addColumn("tên nhân viên");
        tblModel.addColumn("ngày sinh");
        tblModel.addColumn("số điện thoại");
        tblModel.addColumn("hệ số lương");
        tblModel.addColumn("Ngày Công");
        tblModel.addColumn("Lương");
        //--- frmadd lần lượt các row
        for (thongke tk : C) {
            Vector<String> row = new Vector<>();
            row.addElement(String.valueOf(tk.getManv()));
            row.addElement(tk.getTennv());
            row.addElement(String.valueOf(tk.getNgaysinh()));
            row.addElement(tk.getSdt());
            row.addElement(String.valueOf(tk.getHesoluong()));
            row.addElement(String.valueOf(tk.getNgaycong()));
            row.addElement(String.valueOf(tk.getLuong()));
            tblModel.addRow(row);
        }
        dtTTNV.setModel(tblModel);

    }

    public void LoadData3Table() throws SQLException, ClassNotFoundException {
        List<hoadon> c = dao.daohoadon.getALLHD();
        tblModel2.addColumn("Mã hóa đơn");
        tblModel2.addColumn("Tiền bán");
        for (hoadon hd : c) {
            Vector<String> row = new Vector<>();
            row.addElement(String.valueOf(hd.getMahd()));
            row.addElement(String.valueOf(hd.getTienban()));
            tblModel2.addRow(row);
        }
        dtHoaDon.setModel(tblModel2);
        int TongTien = 0;
        for (int i = 0; i < tblModel2.getRowCount(); i++) {
            TongTien = TongTien + Integer.parseInt((String) tblModel2.getValueAt(i, 1));
        }
        jLabel4.setText("" + TongTien + " VND");
    }

