    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {

            DefaultTableModel dtm = (DefaultTableModel) jXTable1.getModel();
            int j = jXTable1.getSelectedRow();
            System.out.println(j);
            if (j != -1) {
                int i = jXTable1.convertRowIndexToModel(j);
                Date ns = new SimpleDateFormat("yyyy-MM-dd").parse((String) dtm.getValueAt(i, 2));
                frmupdatenv frmud = new frmupdatenv();
                frmud.setVisible(true);
                frmud.id.setText((String) dtm.getValueAt(i, 0));
                frmud.nem.setText((String) dtm.getValueAt(i, 1));
                frmud.date.setDate(new SimpleDateFormat("yyyy-MM-dd").parse((String) dtm.getValueAt(i, 2)));
                if ("nam".equals((String) dtm.getValueAt(i, 3))) {
                    frmud.nam.setSelected(true);
                } else {
                    frmud.nu.setSelected(true);
                }
                frmud.que.setText((String) dtm.getValueAt(i, 4));
                frmud.sdt.setText((String) dtm.getValueAt(i, 5));
                frmud.hsl.setText((String) dtm.getValueAt(i, 6));
            }

        } catch (SQLException ex) {
            Logger.getLogger(quanlynhanvien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(quanlynhanvien.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(quanlynhanvien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jXTable1.getModel();
        nhanvien nv1 = new nhanvien();
        int j = jXTable1.getSelectedRow();
        int i = jXTable1.convertRowIndexToModel(j);

        if (j != -1) {
            try {
                nv1.setManv((String) dtm.getValueAt(i, 0));

                daonhanvien.DeleteNV(nv1);
                LoadData2Table();
            } catch (SQLException ex) {
                Logger.getLogger(quanlynhanvien.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(quanlynhanvien.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_delActionPerformed

