    public void LoadData2Table() throws SQLException, ClassNotFoundException {
        List<nhanvien> b = dao.daonhanvien.getALLNV();
        //--- Thêm các tiêu đề cho cột của bảng hiển thị
        tblModel = new DefaultTableModel();
        tblModel.addColumn("mã nhân viên");
        tblModel.addColumn("tên nhân viên");
        tblModel.addColumn("ngày sinh");
        tblModel.addColumn("giới tính");
        tblModel.addColumn("quê quán");
        tblModel.addColumn("số điện thoại");
        tblModel.addColumn("hệ số lương");
        //--- frmadd lần lượt các row
        for (nhanvien nhanvien1 : b) {
            Vector<String> row = new Vector<>();
            row.addElement(String.valueOf(nhanvien1.getManv()));
            row.addElement(nhanvien1.getTennv());
            row.addElement(String.valueOf(nhanvien1.getNgaysinh()));
            row.addElement(nhanvien1.getGioitinh());
            row.addElement(nhanvien1.getQuequan());
            row.addElement(nhanvien1.getSdt());
            row.addElement(String.valueOf(nhanvien1.getHesoluong()));
            tblModel.addRow(row);
        }
        jXTable1.setModel(tblModel);
    }


